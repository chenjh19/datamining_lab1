function Feature1Extraction_Inv()
[input1, input2, input3] = textread('Char_Index.txt','%d %d %s',1000, 'headerlines',1);%读取图片的编号，类别信息和文件名
indexFileName = [input3;input3]; %  复制1000
fid=fopen('feature1_Inv.txt','w+'); % 打开feature1.txt，用以储存特征1的数据
for k=1:2000 %共2000个图片
    if k<1001
        A=imread(strcat('Char_Image_Binary\',char(indexFileName(k,1))));%读入图片
    else
        A=imread(strcat('Char_Image_Binary_Inv\',char(indexFileName(k,1))));%读入图片
    end
    
t=graythresh(A); %取阈值
B=im2bw(A,t);%二值化，B为二值化后的图像矩阵，每个元素的值为0或1
[a,b]=size(B);
C=zeros(1,a+b);%定义特征向量

for i=1:a
    for j=1:b
        if(B(i,j)==1)
            C(1,i)=C(1,i)+1;%如果行中的元素为白色，则增加1，最终结果为每一行的白点数
        end
    end
end

for j=1:b
    for i=1:a
        if(B(i,j)==1)
            C(1,a+j)=C(1,a+j)+1;%最终结果为每一列的白点数
        end
    end
end


fprintf(fid,'%d',k);
fprintf(fid,'%s','       ');
for i=1:a+b-1
  fprintf(fid,'%d',C(1,i));%将特征向量写入文本
  fprintf(fid,'%s',',');%用逗号隔开
end
if k~=2000
fprintf(fid,'%d\n',C(1,a+b));%不为最后一行，则每行末尾加回车
else
fprintf(fid,'%d',C(1,a+b)); %最后一行则不加
end

end
fclose(fid);%关闭文件
